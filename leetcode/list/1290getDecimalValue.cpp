/**********************************************************************************************************
输入：head = [1,0,1]
输出：5
解释：二进制数 (101) 转化为十进制数 (5)
**********************************************************************************************************/
class Solution {
public:
    int getDecimalValue(ListNode* head) {
        ListNode* cur = head;
        int ans = 0;
        while (cur != nullptr) {
            ans = ans * 2 + cur->val;
            cur = cur->next;
        }
        return ans;
    }
};

